<h1>Apa Itu Framework ? </h1>
<p> framework adalah sebuah kerangka program yang digunakan untuk membantu developer untuk mengembangkan kode secara konsisten. Dengan adanya framework developer bisa mengurangi jumlah bug pada website atau aplikasi yang dibuat..</p>
<p>Framework sengaja diciptakan untuk membantu developer mengembangkan aplikasi atau website lebih cepat serta tersusun dan terstruktur. </p>
